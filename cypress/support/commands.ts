Cypress.Commands.add('isElementExist', (elmt) => {
    expect(elmt).to.not.equal('')
    cy.get('body', { timeout: 5000 }).then(($el) => {
        if ($el.has(elmt).length > 0) {
            cy.wrap(true)
        } else {
            cy.log("Element " + elmt + " does not exist")
            cy.wrap(false)
        }
    })
})